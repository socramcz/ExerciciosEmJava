public class TestandoOperacoes {

    public static void main(String[] args) {
        System.out.println("Ola, vamos fazer umas contas");
        teste(10, 5);

    }

    static void teste(int x, int y) {
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x/y);
        System.out.println(x*y);
    }
}
